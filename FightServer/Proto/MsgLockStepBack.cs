using System;
/// <summary>
/// 用于服务端返回给客户端的协议
/// </summary>
public class MsgLockStepBack : MsgBase
{
    /// <summary>
    /// 当前服务端的帧数
    /// </summary>
    public int turn;
    /// <summary>
    /// 未同步的所有操作
    /// </summary>
    public UnsyncOpts[] unsyncOpts;
}
/// <summary>
/// 未同步的操作
/// </summary>
[Serializable]
public class UnsyncOpts
{
    /// <summary>
    /// 帧数
    /// </summary>
    public int turn;
    /// <summary>
    /// 操作
    /// </summary>
    public Opts[] opts;
}